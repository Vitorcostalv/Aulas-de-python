# Aula 4: Listas e Tuplas
# Lista de frutas
frutas = ["maçã", "banana", "laranja"]
print("Lista de frutas:", frutas)

# Adicionando um item na lista
frutas.append("uva")
print("Lista atualizada:", frutas)

# Tupla de números
numeros = (1, 2, 3, 4, 5)
print("Tupla de números:", numeros)

# Acessando elementos na lista e na tupla
print("Primeira fruta:", frutas[0])
print("Primeiro número:", numeros[0])
